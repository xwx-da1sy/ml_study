"""
多元线性回归：使用梯度下降求解

这个文件会按照“从零开始、一步一步补全”的方式来写。
当前阶段我们先完成最基础的准备工作：

1. 项目介绍
2. 导包
3. 创建数据集

后面我们会继续在这个文件里补充：

4. 定义损失函数
5. 计算梯度
6. 使用梯度下降更新参数
7. 观察损失是否下降
8. 得到最终模型公式

为什么这个例子叫“多元线性回归”：
因为每个样本不只有 1 个特征，而是有多个特征一起影响预测结果。

本例中的三个特征分别是：
    x1 -> 学习时间（小时）
    x2 -> 睡眠时间（小时）
    x3 -> 练习题数量（道）

预测目标是：
    y  -> 考试分数

从机器学习角度看：
    X 是特征矩阵
    y 是标签向量

从线性代数角度看：
    X 的每一行是一个样本
    X 的每一列是一个特征
    y 中的每个值都和 X 中同一行样本一一对应
"""


# =========================
# 1. 导包
# =========================

# NumPy 是机器学习里最常用的数值计算工具。
# 这里我们主要用它来：
# 1）创建特征矩阵 X
# 2）创建标签向量 y
# 3）后面进行向量、矩阵和梯度下降运算
import numpy as np


# =========================
# 2. 创建数据集
# =========================

# 下面的 X 是“特征矩阵”。
# 记法：X.shape = (样本数, 特征数)
#
# 每一行表示一个样本。
# 每一列表示一个特征。
#
# 当前这个数据集一共有 12 个样本，每个样本有 3 个特征：
# 第 1 列：学习时间（小时）
# 第 2 列：睡眠时间（小时）
# 第 3 列：练习题数量（道）
X = np.array(
    [
        [1.0, 6.0, 10.0],
        [1.5, 6.5, 12.0],
        [2.0, 7.0, 15.0],
        [2.5, 6.0, 18.0],
        [3.0, 7.5, 20.0],
        [3.5, 7.0, 22.0],
        [4.0, 8.0, 25.0],
        [4.5, 7.5, 28.0],
        [5.0, 8.0, 30.0],
        [5.5, 8.5, 32.0],
        [6.0, 8.0, 35.0],
        [6.5, 8.5, 38.0],
    ],
    dtype=float,
)

# 下面的 y 是“标签向量”，也就是每个样本对应的真实分数。
# 它和 X 按行一一对应：
# X 的第 1 行样本 -> y 的第 1 个分数
# X 的第 2 行样本 -> y 的第 2 个分数
# ...
y = np.array(
    [43.0, 47.0, 53.0, 56.0, 63.0, 66.0, 73.0, 76.0, 81.0, 85.0, 90.0, 95.0],
    dtype=float,
)

# 给三个特征起名字，方便后面打印和解释模型系数。
feature_names = ["study_hours", "sleep_hours", "practice_count"]

# 这里先打印一下数据集的基本结构，帮助初学阶段建立“矩阵感”。
print("X 的形状（样本数, 特征数）:", X.shape)
print("y 的形状（样本数,）:", y.shape)
print()

print("第 1 个样本的特征：", X[0])
print("第 1 个样本的标签：", y[0])
print()

print("特征名称：", feature_names)
